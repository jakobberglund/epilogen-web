    <h2>Biografi</h2>
    <p>
      Den 14 april släpper Epilogen sitt tredje album, Nollpunkten. Det är på många sätt en
      syskonskiva till Dekonstruktion, bandets andra album som släpptes i december förra året, men
      ändå med en tydlig egen identitet. På Nollpunkten arbetar Epilogen med att skapa en egen
      ljudvärld, där de långa låtarna tar avstamp i skenbart enkla strukturer där atmosfärer
      kompromisslöst skulpteras fram med ett anammande av tillfälligheter och improvisation. Det är
      en skiva som växer och förändras med varje lyssning. Musikaliskt är det en mörk blandning av
      rock, elektronik och rostiga analoga industriella ljudlandskap.
    </p>
    <p>
      Epilogen är Stockholmsduon Jakob Berglund på sång, synthar och maskiner, och Anders Carlström
      på gitarrer och bas. Anders och Jakob är vänner sedan barndomen, och har länge spelat musik,
      var för sig och tillsammans. Jakob är medlem i A Swarm of the Sun, där även Anders spelar bas
      live och på skiva. Anders spelar även i The Isolation Process och Kausal.
    </p>
    <p>Nollpunkten är producerad och mixad av Jakob Berglund.</p>
